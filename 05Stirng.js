// Sample string
let str = "Hello, World!";

// Length of the string
console.log("Length:", str.length);

// Convert to uppercase
console.log("Uppercase:", str.toUpperCase());

// Convert to lowercase
console.log("Lowercase:", str.toLowerCase());

// Extracting a substring
console.log("Substring (2-7):", str.substring(2, 7));

// Extracting a substring from the end
console.log("Substring from the end (-6):", str.substr(-6));

// Finding index of a substring
console.log("Index of 'World':", str.indexOf("World"));

// Finding last index of a substring
console.log("Last index of 'o':", str.lastIndexOf("o"));

// Checking if the string starts with a certain substring
console.log("Starts with 'Hello':", str.startsWith("Hello"));

// Checking if the string ends with a certain substring
console.log("Ends with 'World!':", str.endsWith("World!"));

// Replacing part of a string with another string
console.log("Replace 'World' with 'Universe':", str.replace("World", "Universe"));

// Splitting the string into an array of substrings
console.log("Split by ',':", str.split(","));

// Removing leading and trailing whitespaces
let strWithWhitespaces = "   Trim Me    ";
console.log("Original string with whitespaces:", strWithWhitespaces);
console.log("Trimmed string:", strWithWhitespaces.trim());

// Checking if the string contains a certain substring
console.log("Contains 'World':", str.includes("World"));

